package boardgame.model;

/**
 * The Square enum represents the possible states of a square on the game board.
 */
public enum Square {
    /**
     * Represents an empty square.
     */
    NONE,

    /**
     * Represents a square with the "head" piece.
     */
    HEAD,

    /**
     * Represents a square with the "tail" piece.
     */
    TAIL,

    /**
     * Represents a blank square (no piece).
     */
    BLANK
}
